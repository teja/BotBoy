     */
    private val onImageAvailableListener = object : ImageReader.OnImageAvailableListener {
        override fun onImageAvailable(reader: ImageReader?) {

            var im = reader?.acquireLatestImage()
            if (imgTransformer == null && im != null) {
                imgTransformer = ImageTransformer(im!!.width, im!!.height,
                    320, 320)
                outputBitmap = Bitmap.createBitmap(320, 320, Bitmap.Config.ARGB_8888)
                imgTransformer!!.transformImage(im!!, outputBitmap!!, context!!)
            }
            if (imageDetectionSemaphore.tryAcquire()) {
                try {
                    Log.i("TEJA", "Calling get ball locs")
                     val ballLocs = ballDetectHelper!!.getBallLocs(im!!)
                    Log.i("TEJA", "ball locations received")
                    // botDrivingHelper.update(ballLocs)
                } finally {
                    imageDetectionSemaphore.release()
                    im?.close()
                }
            } else {
                Log.i("TEJA", "Got new img but dropping because previous ball is still being processed")
                im?.close()
            }
            //view3.AddRect()
        }
    }

